package e_oop;

public class Calculator {

	double aa(double a, double b){
		return a + b;
	}
	
	double bb(double a, double b){
		return a - b;
	}
	
	double cc(double a, double b){
		return a * b;
	}
	
	double dd(double a, double b){
		return a / b;
	}
	
	double ee(double a, double b){
		return a % b;
	}
	
}
